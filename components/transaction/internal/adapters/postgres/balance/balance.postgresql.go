// BalancesUpdate updates the balances in the database.
func (r *BalancePostgreSQLRepository) BalancesUpdate(ctx context.Context, organizationID, ledgerID uuid.UUID, balances []*mmodel.Balance) error {
	logger, tracer, _, _ := libCommons.NewTrackingFromContext(ctx)

	_, span := tracer.Start(ctx, "postgres.update_balances")
	defer span.End()

	db, err := r.connection.GetDB()
	if err != nil {
		libOpentelemetry.HandleSpanError(&span, "Failed to get database connection", err)

		return err
	}

	tx, err := db.BeginTx(ctx, nil)
	if err != nil {
		libOpentelemetry.HandleSpanError(&span, "Failed to init balances", err)

		return err
	}

	defer func() {
		if err != nil {
			rollbackErr := tx.Rollback()
			if rollbackErr != nil {
				libOpentelemetry.HandleSpanError(&span, "Failed to init balances", rollbackErr)

				logger.Errorf("err on rollback: %v", rollbackErr)
			}
		} else {
			commitErr := tx.Commit()
			if commitErr != nil {
				libOpentelemetry.HandleSpanError(&span, "Failed to init balances", commitErr)

				logger.Errorf("err on commit: %v", commitErr)
			}
		}
	}()

	for _, balance := range balances {
		ctxBalance, spanUpdate := tracer.Start(ctx, "postgres.update_balance")

		var updates []string

		var args []any

		updates = append(updates, "available = $"+strconv.Itoa(len(args)+1))
		args = append(args, balance.Available)

		updates = append(updates, "on_hold = $"+strconv.Itoa(len(args)+1))
		args = append(args, balance.OnHold)

		updates = append(updates, "version = $"+strconv.Itoa(len(args)+1))
		args = append(args, balance.Version)

		updates = append(updates, "updated_at = $"+strconv.Itoa(len(args)+1))
		args = append(args, time.Now())

		// Add WHERE clause parameters
		args = append(args, organizationID, ledgerID, balance.ID, balance.Version)

		queryUpdate := `UPDATE balance SET ` + strings.Join(updates, ", ") +
			` WHERE organization_id = $` + strconv.Itoa(len(args)-3) +
			` AND ledger_id = $` + strconv.Itoa(len(args)-2) +
			` AND id = $` + strconv.Itoa(len(args)-1) +
			` AND version < $` + strconv.Itoa(len(args)) +
			` AND deleted_at IS NULL`

		result, err := tx.ExecContext(ctxBalance, queryUpdate, args...)
		if err != nil {
			libOpentelemetry.HandleSpanError(&spanUpdate, "Err on result exec content", err)

			logger.Errorf("Err on result exec content: %v", err)

			return err
		}

        rowsAffected, err := result.RowsAffected()
        if err != nil {
            libOpentelemetry.HandleSpanError(&spanUpdate, "Err ", err)

            logger.Errorf("Err: %v", err)

            return err
        }

        if rowsAffected == 0 {
            // No row updated means a lost update race (version not less than provided).
            // Surface a business error so upstream can retry or reject instead of silently ACKing.
            return pkg.ValidateBusinessError(constant.ErrLockVersionAccountBalance, reflect.TypeOf(mmodel.Balance{}).Name())
        }

		spanUpdate.End()
	}

	return nil
}

// BalancesIncrement applies incremental changes to balances in a single transaction.
func (r *BalancePostgreSQLRepository) BalancesIncrement(ctx context.Context, organizationID, ledgerID uuid.UUID, deltas []mmodel.BalanceDelta) error {
    logger, tracer, _, _ := libCommons.NewTrackingFromContext(ctx)

    _, span := tracer.Start(ctx, "postgres.increment_balances")
    defer span.End()

    if len(deltas) == 0 {
        return nil
    }

    db, err := r.connection.GetDB()
    if err != nil {
        libOpentelemetry.HandleSpanError(&span, "Failed to get database connection", err)
        return err
    }

    tx, err := db.BeginTx(ctx, nil)
    if err != nil {
        libOpentelemetry.HandleSpanError(&span, "Failed to start transaction", err)
        return err
    }
    defer func() {
        if err != nil {
            _ = tx.Rollback()
        } else {
            _ = tx.Commit()
        }
    }()

    for _, d := range deltas {
        // Update available/on_hold atomically and bump version
        // We do not guard on version here; idempotency is enforced at the operation level.
        query := `UPDATE balance 
                  SET available = available + $1,
                      on_hold   = on_hold + $2,
                      version   = version + 1,
                      updated_at= $3
                  WHERE organization_id = $4 AND ledger_id = $5 AND id = $6 AND deleted_at IS NULL`
        _, err = tx.ExecContext(ctx, query,
            d.DeltaAvailable,
            d.DeltaOnHold,
            time.Now(),
            organizationID,
            ledgerID,
            uuid.MustParse(d.ID),
        )
        if err != nil {
            libOpentelemetry.HandleSpanError(&span, "Failed to increment balance", err)
            logger.Errorf("Failed to increment balance %s: %v", d.ID, err)
            return err
        }
    }

    return nil
}

